    <p>El concepto original de SEAN se gestó en un área rural del sur de Chile en los años 50. Su creador fue Antonio Barratt, 
        un misionero Anglicano de la SAMS (Sociedad Misionera Sudamericana). Allí, vio la necesidad de comenzar un Instituto Bíblico y Centro de Capacitación 
        Agrícola para las iglesias Mapuches.</p> 

    <p>Estos, eran inteligentes, capaces y voluntariosos, pero debido a su mínima educación escolar y el hecho de tener al Español como segundo idioma, 
        no podían entrar en los Seminarios de la ciudad. Además, debido a su condición de agricultores, no podían abandonar sus campos y familias para estudiar.
        Así, Antonio experimentó con un nuevo sistema de cursos autodidácticos y estudio en grupo para revisar lo aprendido. Este sistema dio generosos dividendos, 
        pero sólo fue usado a un nivel local y reducido.</p> 

    <p> El pastor Barratt siguió su ministerio en Paraguay y luego en Tucumán, Argentina. En ese tiempo, 1971, el concepto de ETE (Educación Teológica por Extensión), 
        gestado en Guatemala, ya se había popularizado a través del mundo. ETE, con su objetivo de llevar el Seminario a las iglesias locales, 
        fue la plataforma para que los cursos originados por Antonio llegaran a perfeccionarse y ser conocidos.</p> 

    <p>En el año 1971, a pedido de la Iglesia, Antonio formó un pequeño equipo de pastores y profesores de Argentina, Chile y Paraguay, y fundó formalmente a SEAN, 
        que en un inicio significó Seminario por Extensión ANglicano. Pero pronto, por su crecimiento, SEAN fue registrado como una fundación internacional e interdenominacional 
        con su nombre actual - Seminario por Extensión A las Naciones.</p> 

    <p>Los primeros cursos fueron Vida Abundante y el Compendio de Teología Pastoral - 6 volúmenes basados en la vida de Cristo.</p> 

    <p>Se establecieron los objetivos pedagógicos y prácticos y se repartió la tarea entre el equipo de escritores en sus países de residencia. 
        Estos elaboraban y probaban las lecciones asignadas y luego las enviaban al equipo editor en Tucumán.</p> 

    <p>Fue una tarea titánica. Para hacer que las lecciones fueran simples para los estudiantes pero profundas en contenido, el equipo dedicó 600 horas de preparación para cada 
        lección. De hecho, el Compendio de Teología Pastoral se inició en el año 71 y fue terminado en el año 77. Cada lección fue ser rigurosamente probada y corregida hasta que 
        80% de los estudiantes pudieran lograr 80% de los objetivos.</p> 

    <p>Todo el trabajo final se hizo en la casa y garage de la familia Barratt. Nadie se escapaba, ni siquiera las visitas. Fueron varios los obispos y dignatarios visitantes 
        que debieron caminar alrededor de la mesa del comedor compaginando cursos. ¡Eran alentados por el hecho de que debían despejar la mesa para poder almorzar!</p> 

    <p>En los primeros años esta obra artesanal sólo fue usado a nivel local. SEAN nunca publicitó sus cursos, pero gradualmente se comenzó a correr la voz y hubo una creciente 
        demanda de usuarios de las más diversas denominaciones a través de América Latina y luego del mundo entero.</p> 

    <p>Eventualmente la tarea de escribir, duplicar, armar y enviar los materiales a tantas partes se transformó en una pesadilla para tan pequeño grupo y así surgió la idea que 
        SEAN se concentrara solo en el escribir los cursos y entregar la licencia a los países para que imprimieran en forma local.</p> 

    <p>Actualmente los cursos SEAN se usan en unos 100 Países y en más de 70 Idiomas.</p> 

    <p>Antonio Barratt y la mayoría de los autores originales ya están jubilados, pero el ministerio continúa. El pequeño equipo SEAN sigue escribiendo, corrigiendo y 
        modernizando los cursos.</p> 

    <p>SEAN es una Sociedad Misionera, pero sin Misioneros; es un Ministerio sin muchos Ministros. SEAN provee a las Misiones e Iglesias con herramientas de trabajo, 
        para que capaciten a sus obreros y estos últimos hagan el ministerio y la misión. Y lo hacen con excelencia en Mongolia y en Mozambique; China y Chile; Uzbekistan y Ulster; 
        Albania y Axminster, y estos son sólo algunos de los lugares donde se encuentran los cursos SEAN.</p>   
