    <p class="contenido">Un estudio minucioso y detallado del libro de Génesis y de una armonía del resto del Pentateuco, en el que destaca, principalmente, el tema mesiánico. 
        2 libros para 6 meses de estudio.</p>
    <a class="volver" href="#indice_cursos">Volver al índice</a>
    <h3 class="libro" id="jeremias">Jeremías</h3>
    <p class="contenido">Jeremías es el curso más avanzado de SEAN. Es programado y accesible, pero se requiere de mas estudio de parte del estudiante, 
        por lo tanto se recomienda para personas avanzadas en el estudio de las sagradas escrituras (pastores y ministros).El libro de Jeremías es complejo, 
        y su estructura confusa; con frecuencia desanima a quienes buscan estudiarlo a fondo, y por ello no se descubre su valioso contenido. Es por esto, que SEAN ha 
        desarrollado este nuevo curso para que el libro sea accesible y de gran estímulo para quienes deseen profundizar en la vida, los tiempos, el ministerio y los mensajes 
        de uno de los más grandes profetas del Antiguo Testamento.</p>
