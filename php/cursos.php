    <h2 class="seccion" id="segundo_nivel">Segundo Nivel</h2>
    <p class="contenido">Al completar este nivel usted recibe un Diploma en teología Básica</p>
    <h3 class="libro" id="compendio">La Vida de Jesucristo. Compendio de teología pastoral, basado en el evangelio de Mateo.</h3>
    <p class="contenido">Esta es la obra principal de SEAN y es un curso integral que nos instruye a tener un conocimiento amplio y practico de la vida de Jesucristo, 
        desde sus anuncios proféticos hasta la muerte, resurrección y ascensión al cielo, para obreros y ministros del Señor Jesús, basado en el evangelio de Mateo y 
        Hechos de los apóstoles.El curso consta de 6 libros de 10 unidades semanales cada uno. Cada libro puede ser cursado en 2 meses y el programa total es flexible 
        entre 1 año a 1 año y medio.</p>
    <a class="volver" href="#indice_cursos">Volver al índice</a>
