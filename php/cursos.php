    <h3 class="libro" id="trabajo">Trabajo para todos</h3>
    <p class="contenido">Este curso presenta los principios bíblicos y éticos del trabajo. Hace ver que el trabajo es una bendición de Dios y enseña como usarlo para honrar al creador 
        y serir al prójimo. A diferencia de otros cursos SEAN que dan lecciones para hacer en la casa y luego se aplica lo aprendido en reuniones de grupo llamadas “Peñas”, 
        este curso fue preparado para ser hecho siempre en grupo, junto a un Tutor.</p>
