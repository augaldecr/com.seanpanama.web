    <h3 class="libro" id="encuentros">Manual de Encuentros Bíblicos</h3>
    <p class="contenido">Colección de más de 75 guías de estudios bíblicos (lecturas, preguntas y respuestas) sobre distintos temas: evangelización, doctrina, relaciones interpersonales, 
        crecimiento cristiano, problemas sociales, emocionales y espirituales, cómo temas de actualidad. Se utilizan los temas según la necesidad del momento. Recomendado para 
        líderes de grupos de estudio bíblico.</p>
